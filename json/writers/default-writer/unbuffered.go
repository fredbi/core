// Unbuffered JSON writer.
// [Unbuffered] implements
// [writers.StoreWriter], [writers.JSONWriter], [writers.TokenWriter] and [writers.VerbatimWriter].
//
// It knows how to render JSON tokens and JSON values to an underlying [io.Writer].
//
// All writes are passed straight-through, and no flushing is required.
//
// Strings are escaped with default JSON escaping rule for tabs, new lines, line feeds, backslashes and double quotes.
//
// There is no attempt to do anything special regarding empty or null values:
//
//   - an undefined value (or nil data) is not rendered
//   - a null value is necessarily defined explicitly and is rendered with the "null" token
//
// # Concurrency
//
// [Unbuffered] is not intended for concurrent use.
type Unbuffered struct {
	unbuffered
	commonWriter[*unbuffered]
}

type unbuffered struct {
	baseWriter

	bw      io.ByteWriter // w.w asserted to io.ByteWriter once (nil if unsupported); avoids a per-call type assertion
	scratch [1]byte       // reused for single-byte writes to avoid a per-call allocation
}

// NewUnbuffered JSON writer that copies JSON to [io.Writer] w, without buffering.
//
// [Unbuffered] has no configurable options and holds no pooled resources, so there is nothing to
// relinquish: no options finalizer is registered.
func NewUnbuffered(w io.Writer, _ ...UnbufferedOption) *Unbuffered {
	writer := &Unbuffered{
		unbuffered: unbuffered{
			baseWriter: baseWriter{
				w: w,
			},
		},
	}
	writer.jw = &writer.unbuffered
	writer.bw, _ = w.(io.ByteWriter)

	return writer
}

func (w *Unbuffered) Reset() {
	w.baseWriter.Reset()
}

// redeem has no inner resources to relinquish for the Unbuffered writer.
func (w *unbuffered) redeem() {}

func (w *unbuffered) writeSingleByte(c byte) {
	if w.err != nil {
		return
	}

	if w.bw != nil {
		w.err = w.bw.WriteByte(c)
		if w.err == nil {
			w.inc(1)
		}

		return
	}

	// w.scratch is part of the (heap-allocated) writer, so slicing it does not allocate,
	// unlike a fresh []byte{c} literal which escapes through the io.Writer interface.
	w.scratch[0] = c
	n, err := w.w.Write(w.scratch[:])
	w.err = err
	w.inc(n)
}

func (w *unbuffered) writeBinary(data []byte) {
	n, err := w.w.Write(data)
	w.inc(n)
	w.err = err
}

// writeEscaped escapes data and writes it straight through to the underlying writer.
//
// Like [buffered.writeEscaped], it returns any trailing bytes that form an incomplete UTF-8
// rune (e.g. a multi-byte rune split across a read boundary in [commonWriter.StringCopy]),
// rather than treating them as an error. The caller decides whether the remainder is a
// genuine truncation or should be stitched with the next read.
func (w *unbuffered) writeEscaped(data []byte) []byte {
	if w.err != nil {
		return nil
	}

	escapedHolder, redeemEscaped := poolOfEscapedBuffers.BorrowWithSizeAndRedeem(len(data))
	escapedBuffer := escapedHolder.Slice()
	escapedBuffer, remainder := escapedBytes(data, escapedBuffer)
	w.writeBinary(escapedBuffer)
	redeemEscaped()

	// remainder points into data (the caller's buffer), so it stays valid after redeem.
	return remainder
}

